void AASCRotTableFromTemplate(struct _AASCRot *ptr, int nbRot, int nbAtoms, int *atypes, double *charges, 
			      double *coords, char *atnames, char *name) {
  ptr->nbRot = nbRot;
  ptr->nbAtoms = nbAtoms;
  ptr->atypes = (int *)malloc(nbAtoms*sizeof(int));
  ptr->charges = (double *)malloc(nbAtoms*sizeof(double));
  ptr->name = (char *)malloc((strlen(name)+1)*sizeof(char));
  strcpy(ptr->name, name);
  ptr->coords = (double **)malloc(nbRot*sizeof(double*));
  ptr->atnames = (char *)malloc((strlen(atnames)+1)*sizeof(char));
  strcpy(ptr->atnames, atnames);
  for (int i=0; i<ptr->nbAtoms; i++) {
    ptr->atypes[i] = atypes[i];
    ptr->charges[i] = charges[i];
  }
  for (int i=0; i<ptr->nbRot; i++) {
    double *c = malloc(ptr->nbAtoms*3*sizeof(double));
    int n=0;
    for (int j=0; j<ptr->nbAtoms; j++) {
      for (int k=0; k<3; k++) {
	c[n] = coords[i*nbAtoms*3 + j*3 + k];
	n+=1;
      }
    }
    ptr->coords[i] = c;
  }
}

struct _AASCRot *_AASCRotTable;
int nbCanAA = 0;

int getSideChainTemplateIndexFromName(char *str) {
  for (int i=0; i<nbCanAA; i++) {
    if (strcmp(str, _AASCRotTable[i].name)==0) return i;
  }
  return -1;
}

// 1 letter code for std AA
char IDchars[18] = "RNDCQEHILKMFPSTWYV";

int getSideChainTemplateIndexFromIDchar(char id) {
  char *c = IDchars;
  for (int i=0; i<18; i++) {
    if (c[i]==id) return i;
  }
  return -1;
}

